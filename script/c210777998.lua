	local function finish_setup() 
		--Pre-draw
		local e1=Effect.GlobalEffect()
		e1:SetType(EFFECT_TYPE_FIELD+EFFECT_TYPE_CONTINUOUS)
		e1:SetCode(EVENT_STARTUP)
		e1:SetCountLimit(1)
		e1:SetOperation(BoosterDraft.op)
		Duel.RegisterEffect(e1,0)
	end
	function BoosterDraft.op(e,tp,eg,ep,ev,re,r,rp)
		local z,o=tp,1-tp
		--Hint
		local counts={}
		counts[0]=Duel.GetPlayersCount(0)
		counts[1]=Duel.GetPlayersCount(1)
		Duel.DisableShuffleCheck()
		Duel.Hint(HINT_CARD,0,id)

		for p=z,o do
			for team=1,counts[p] do
				Duel.SendtoDeck(Duel.GetFieldGroup(tp,0xff,0xff),0,-2,REASON_RULE)
				if counts[p]~=1 then
					Duel.TagSwap(p)
				end
			end
		end

		local groups={}
		groups[0]={}
		groups[1]={}
		for i=1,counts[0] do
			groups[0][i]={}
		end
		for i=1,counts[1] do
			groups[1][i]={}
		end

		local function generate_packs()
			local total=(counts[0]+counts[1])*3
			Debug.Message("Total = "..tostring(total))
			local retpacks={}
			for t=1,total do
				local g=Group.CreateGroup()
				for p=1,3 do
					for i=1,6 do
						local cpack=pack[i]
						local c=cpack[Duel.GetRandomNumber(1,#cpack)]
						g:AddCard(Debug.AddCard(c,t%2,t%2,LOCATION_GRAVE,1,POS_FACEUP))
					end
				end
				table.insert(retpacks,g)
			end
			Debug.ReloadFieldEnd()
			return retpacks
		end
		local packs = generate_packs()
		local graveg=Duel.GetFieldGroup(p,LOCATION_GRAVE,LOCATION_GRAVE)
		--[[
		local pack=table.remove(packs, 1)
		local confirmed=false
		while pack do
			for p=z,o do
				for team=1,counts[p] do
					if not confirmed then Duel.ConfirmCards(p,graveg) end
					local tc=pack:Select(p,1,1,nil)
					table.insert(groups[p][team],tc:GetFirst():GetCode())
					pack:Sub(tc)
					if counts[p]~=1 then
						Duel.TagSwap(p)
					end
					if #pack==0 then
						pack=table.remove(packs, 1)
						if not pack then goto exit end
					end
				end
			end
			confirmed=true
		end--]]
		::exit::
		for p=z,o do
			for team=1,counts[p] do
				local graveg=Duel.GetFieldGroup(p,LOCATION_GRAVE,0)
				local tempg=Group.CreateGroup()
				for tc in aux.Next(graveg) do
					tc:Recreate(groups[p][team][#tempg+1],nil,nil,nil,nil,nil,nil,nil,nil,nil,nil,nil,true)
					tempg:AddCard(tc)
					if #tempg==#groups[p][team] then break end
				end
				Duel.SendtoDeck(tempg,nil,2,REASON_RULE)
				Duel.ShuffleDeck(p)
				if counts[p]~=1 then
					Duel.TagSwap(p)
				end
			end
		end
		e:Reset()
	end
	finish_setup()
